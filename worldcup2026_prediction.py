import pandas as pd
import sqlite3
import os

url = "https://raw.githubusercontent.com/martj42/international_results/master/results.csv"
raw_df = pd.read_csv(url)
raw_df.head()

raw_df['date'].max()

# matches from 2022-01-01 onwards and the tournaments that matter
#FIFA World Cup finals (2022)
#FIFA World Cup qualification (all confederations)
#Continental championships (Euro, Copa América, AFCON, Asian Cup, Gold Cup)
#Some friendlies (but we can decide later)

#converts the data to datatime
raw_df['date']=pd.to_datetime(raw_df['date'])

#so we have to keep the matches since 2022-01-01
recent = raw_df[raw_df['date'] >='2022-01-01']
print("matches since 2022:",len(recent))

#now we have to select which tournaments we want
# Tournament names to include (case-insensitive will be handled)
tournament_filters = [
    'FIFA World Cup',            # catches 'FIFA World Cup' and 'FIFA World Cup qualification'
    'UEFA Euro',                 # 2024
    'Copa América',              # 2021/2024
    'Africa Cup of Nations',     # AFCON 2023
    'AFC Asian Cup',             # 2023
    'CONCACAF Gold Cup'          # 2023
]

raw_df.head()

mask = recent['tournament'].str.contains('|'.join(tournament_filters), case=False, na=False)
relevant = recent[mask].copy()
print("Matches after filtering:", len(relevant))

relevant['tournament'].value_counts().head(15)

raw_df.head()

# Create a connection to a new database (or connect to existing one)
conn = sqlite3.connect('world_cup_dw.db')
cursor = conn.cursor()

# ----- Create Team Dimension -----
cursor.execute('''
CREATE TABLE IF NOT EXISTS Team_Dim (
    team_id INTEGER PRIMARY KEY AUTOINCREMENT,
    team_name TEXT UNIQUE,
    confederation TEXT
)
''')

# ----- Create Tournament Dimension -----
cursor.execute('''
CREATE TABLE IF NOT EXISTS Tournament_Dim (
    tournament_id INTEGER PRIMARY KEY AUTOINCREMENT,
    tournament_name TEXT UNIQUE,
    tournament_type TEXT   -- e.g., 'World Cup', 'Qualifier', 'Continental'
)
''')

# ----- Create Date Dimension (simplified) -----
cursor.execute('''
CREATE TABLE IF NOT EXISTS Date_Dim (
    date_id INTEGER PRIMARY KEY AUTOINCREMENT,
    full_date TEXT UNIQUE,
    year INTEGER,
    month INTEGER,
    day INTEGER
)
''')

# ----- Create Fact Table -----
cursor.execute('''
CREATE TABLE IF NOT EXISTS Match_Facts (
    match_id INTEGER PRIMARY KEY AUTOINCREMENT,
    date_id INTEGER,
    home_team_id INTEGER,
    away_team_id INTEGER,
    tournament_id INTEGER,
    home_score INTEGER,
    away_score INTEGER,
    neutral INTEGER,  -- 0 = false, 1 = true
    FOREIGN KEY (date_id) REFERENCES Date_Dim(date_id),
    FOREIGN KEY (home_team_id) REFERENCES Team_Dim(team_id),
    FOREIGN KEY (away_team_id) REFERENCES Team_Dim(team_id),
    FOREIGN KEY (tournament_id) REFERENCES Tournament_Dim(tournament_id)
)
''')

conn.commit()
print("Tables created successfully!")

# ----- Teams -----
# Get all unique team names from home_team and away_team columns
all_teams = pd.concat([relevant['home_team'], relevant['away_team']]).unique()
for team in all_teams:
    # For now, confederation is unknown – we'll fill it later, or you can add manually
    cursor.execute("INSERT OR IGNORE INTO Team_Dim (team_name) VALUES (?)", (team,))
print("Teams inserted.")

# ----- Tournaments -----
for tournament in relevant['tournament'].unique():
    # Determine type
    if 'qualification' in tournament.lower():
        ttype = 'Qualifier'
    elif 'FIFA World Cup' in tournament and 'qualification' not in tournament:
        ttype = 'World Cup'
    else:
        ttype = 'Continental'
    cursor.execute("INSERT OR IGNORE INTO Tournament_Dim (tournament_name, tournament_type) VALUES (?, ?)",
                   (tournament, ttype))
print("Tournaments inserted.")

# ----- Dates -----
for date in relevant['date'].unique():
    # Convert to string in 'YYYY-MM-DD' format
    date_str = date.strftime('%Y-%m-%d')
    cursor.execute("INSERT OR IGNORE INTO Date_Dim (full_date, year, month, day) VALUES (?,?,?,?)",
                   (date_str, date.year, date.month, date.day))
print("Dates inserted.")
conn.commit()

# Load the dimension tables into DataFrames for quick mapping
team_dim_df = pd.read_sql("SELECT * FROM Team_Dim", conn)
tournament_dim_df = pd.read_sql("SELECT * FROM Tournament_Dim", conn)
date_dim_df = pd.read_sql("SELECT * FROM Date_Dim", conn)

# Create dictionaries for fast lookup
team_to_id = dict(zip(team_dim_df['team_name'], team_dim_df['team_id']))
tournament_to_id = dict(zip(tournament_dim_df['tournament_name'], tournament_dim_df['tournament_id']))
date_to_id = dict(zip(date_dim_df['full_date'], date_dim_df['date_id']))

# Now loop through the relevant DataFrame and insert fact rows
for idx, row in relevant.iterrows():
    try:
        home_id = team_to_id[row['home_team']]
        away_id = team_to_id[row['away_team']]
        tourn_id = tournament_to_id[row['tournament']]
        date_id = date_to_id[row['date'].strftime('%Y-%m-%d')]
        neutral_val = 1 if row['neutral'] == True else 0

        cursor.execute('''
            INSERT INTO Match_Facts (date_id, home_team_id, away_team_id, tournament_id, home_score, away_score, neutral)
            VALUES (?,?,?,?,?,?,?)
        ''', (date_id, home_id, away_id, tourn_id, row['home_score'], row['away_score'], neutral_val))
    except Exception as e:
        print(f"Error on row {idx}: {e}")

conn.commit()
print("Fact table populated!")

# Test query: show 5 matches with team names and tournament
query = '''
SELECT
    d.full_date,
    ht.team_name AS home_team,
    at.team_name AS away_team,
    m.home_score,
    m.away_score,
    t.tournament_name,
    t.tournament_type
FROM Match_Facts m
JOIN Date_Dim d ON m.date_id = d.date_id
JOIN Team_Dim ht ON m.home_team_id = ht.team_id
JOIN Team_Dim at ON m.away_team_id = at.team_id
JOIN Tournament_Dim t ON m.tournament_id = t.tournament_id
LIMIT 5
'''

result = pd.read_sql(query, conn)
