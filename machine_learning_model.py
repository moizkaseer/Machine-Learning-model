window_end   = start_2026

# All matches inside that window (up to the day before the tournament)
recent_2026 = all_matches[(all_matches['date'] >= window_start) &
                          (all_matches['date'] < window_end)]

# Host nations (as they appear in the match dataset)
hosts_2026 = ['United States', 'Canada', 'Mexico']

rows_2026 = []

for team in teams_2026_match:
    # Matches involving this team (home or away)
    team_matches = recent_2026[(recent_2026['home_team'] == team) |
                               (recent_2026['away_team'] == team)]
    if len(team_matches) == 0:
        continue   # skip if no recent matches (very unlikely for a qualified team)

    goals_scored = 0
    goals_conceded = 0
    wins = 0
    draws = 0
    losses = 0

    for _, m in team_matches.iterrows():
        if m['home_team'] == team:
            goals_scored += m['home_score']
            goals_conceded += m['away_score']
            if m['home_score'] > m['away_score']:
                wins += 1
            elif m['home_score'] == m['away_score']:
                draws += 1
            else:
                losses += 1
        else:   # team played away
            goals_scored += m['away_score']
            goals_conceded += m['home_score']
            if m['away_score'] > m['home_score']:
                wins += 1
            elif m['away_score'] == m['home_score']:
                draws += 1
            else:
                losses += 1

    total_matches = wins + draws + losses
    win_rate = wins / total_matches
    goals_pg = goals_scored / total_matches
    goals_conceded_pg = goals_conceded / total_matches

    # FIFA rank: look up using the original name_corrections (to convert match‑name → ranking‑name)
