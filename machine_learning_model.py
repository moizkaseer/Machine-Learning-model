    if len(team_ranks) > 0:
        latest_rank = team_ranks.iloc[-1]['rank']
        conf = team_ranks.iloc[-1]['confederation']
    else:
        latest_rank = 200   # fallback
        conf = 'Unknown'

    is_host = 1 if team in hosts_2026 else 0

